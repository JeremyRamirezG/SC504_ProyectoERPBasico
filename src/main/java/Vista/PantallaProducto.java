    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
String palabraClave = jTextField4.getText().trim();
        
        try {
            Connection conn = oracle.getConnection();

            CallableStatement cstmt = conn.prepareCall("{call SP_buscar_producto(?, ?)}");

            cstmt.setString(1, palabraClave);
            cstmt.registerOutParameter(2, OracleTypes.CURSOR);

            cstmt.execute();

            ResultSet rs = (ResultSet) cstmt.getObject(2);

            // Limpiar la tabla antes de agregar nuevos resultados
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (rs.next()) {
                String idProducto = rs.getString("ID_PRODUCTO");
                String descripcion = rs.getString("DESCRIPCION");
                double precio = rs.getDouble("PRECIO");
                String proveedor = rs.getString("PROVEEDOR");
                Date fechaIngreso = rs.getDate("FECHA_INGRESO");
                int cantidad = rs.getInt("CANTIDAD");

                // Agregar fila a la tabla
                model.addRow(new Object[]{idProducto, descripcion, precio, proveedor, fechaIngreso, cantidad});
            }

            rs.close();
            cstmt.close();
            conn.close();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Error al buscar producto: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton3MouseClicked

